CREATE SEQUENCE IF NOT EXISTS password_reset_token_seq START WITH 1 INCREMENT BY 50;

CREATE SEQUENCE IF NOT EXISTS verification_token_seq START WITH 1 INCREMENT BY 50;

CREATE TABLE app_user
(
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    password                VARCHAR(255),
    username                VARCHAR(255)                            NOT NULL,
    first_name              VARCHAR(255),
    last_name               VARCHAR(255),
    account_non_expired     BOOLEAN                                 NOT NULL,
    account_non_locked      BOOLEAN                                 NOT NULL,
    credentials_non_expired BOOLEAN                                 NOT NULL,
    enabled                 BOOLEAN                                 NOT NULL,
    is_using2fa             BOOLEAN                                 NOT NULL,
    provider                VARCHAR(255),
    CONSTRAINT pk_appuser PRIMARY KEY (id)
);

CREATE TABLE be_on_the_look_out
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    version        SMALLINT                                NOT NULL,
    created_at     TIMESTAMP WITHOUT TIME ZONE,
    updated_at     TIMESTAMP WITHOUT TIME ZONE,
    created_by     BIGINT,
    phone_number   VARCHAR(255),
    last_seen_date TIMESTAMP WITHOUT TIME ZONE,
    extra_info     VARCHAR(1024),
    enabled        BOOLEAN DEFAULT FALSE,
    CONSTRAINT pk_beonthelookout PRIMARY KEY (id)
);

CREATE TABLE item
(
    id                 BIGINT                     NOT NULL,
    last_seen_location GEOMETRY(MultiPoint, 4326) NOT NULL,
    CONSTRAINT pk_item PRIMARY KEY (id)
);

CREATE TABLE means_of_transportation
(
    id                     BIGINT                NOT NULL,
    last_seen_location     GEOMETRY(Point, 4326) NOT NULL,
    human_readable_address JSONB,
    color                  VARCHAR(255),
    brand                  VARCHAR(255),
    model                  VARCHAR(255),
    CONSTRAINT pk_meansoftransportation PRIMARY KEY (id)
);

CREATE TABLE password_reset_token
(
    id          BIGINT NOT NULL,
    token       VARCHAR(255),
    app_user_id BIGINT NOT NULL,
    expiry_date TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_passwordresettoken PRIMARY KEY (id)
);

CREATE TABLE pet
(
    id                     BIGINT                NOT NULL,
    last_seen_location     GEOMETRY(Point, 4326) NOT NULL,
    human_readable_address JSONB,
    name                   VARCHAR(255),
    species                VARCHAR(255),
    breed                  VARCHAR(255),
    sex                    VARCHAR(255),
    color                  VARCHAR(255),
    transponder_code       BIGINT,
    CONSTRAINT pk_pet PRIMARY KEY (id)
);

CREATE TABLE role
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255)                            NOT NULL,
    CONSTRAINT pk_role PRIMARY KEY (id)
);

CREATE TABLE sighting
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    version    SMALLINT                                NOT NULL,
    created_at TIMESTAMP WITHOUT TIME ZONE,
    updated_at TIMESTAMP WITHOUT TIME ZONE,
    created_by BIGINT,
    point      BYTEA,
    moment     TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_sighting PRIMARY KEY (id)
);

CREATE TABLE trace
(
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    version               SMALLINT                                NOT NULL,
    be_on_the_look_out_id BIGINT                                  NOT NULL,
    location              GEOMETRY(Point, 4326)                   NOT NULL,
    date                  TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_trace PRIMARY KEY (id)
);

CREATE TABLE users_roles
(
    role_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    CONSTRAINT pk_users_roles PRIMARY KEY (role_id, user_id)
);

CREATE TABLE verification_token
(
    id          BIGINT NOT NULL,
    token       VARCHAR(255),
    app_user_id BIGINT NOT NULL,
    expiry_date TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_verificationtoken PRIMARY KEY (id)
);

